package day12;

public class Point {
    private int east;
    private int north;

    public Point(int east, int north) {
        this.east = east;
        this.north = north;
    }

    public int getEast() {
        return east;
    }

    public void setEast(int east) {
        this.east = east;
    }

    public int getNorth() {
        return north;
    }

    public void setNorth(int north) {
        this.north = north;
    }


}
